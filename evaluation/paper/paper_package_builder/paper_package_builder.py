import json
import os
from datetime import datetime, timezone


class PaperPackageBuilder:

    def __init__(
        self,
        paper_dir="evaluation/results/final/paper",
        output_dir="evaluation/results/final/paper/package",
    ):
        self.paper_dir = paper_dir
        self.output_dir = output_dir

        self.sections_dir = os.path.join(
            paper_dir,
            "sections",
        )

        self.tables_dir = os.path.join(
            paper_dir,
            "tables",
        )

        self.figures_dir = os.path.join(
            paper_dir,
            "figures",
        )

        os.makedirs(
            self.output_dir,
            exist_ok=True,
        )

    def _load_text(self, path):
        with open(
            path,
            "r",
            encoding="utf-8",
        ) as file:
            return file.read().strip()

    def _load_json(self, path):
        with open(
            path,
            "r",
            encoding="utf-8",
        ) as file:
            return json.load(file)

    def _load_section(self, name):
        return self._load_text(
            os.path.join(
                self.sections_dir,
                f"{name}.md",
            )
        )

    def _build_package(self):
        results = self._load_section(
            "results"
        )

        ablation = self._load_section(
            "ablation"
        )

        statistics = self._load_section(
            "statistics"
        )

        discussion = self._load_section(
            "discussion"
        )

        threats = self._load_section(
            "threats_to_validity"
        )

        reproducibility = self._load_section(
            "reproducibility"
        )

        lines = [
            "# CRME Results and Evidence Package",
            "",
            (
                "This package was automatically assembled "
                "from the canonical CRME evaluation evidence "
                "and paper-generation artifacts."
            ),
            "",
            "---",
            "",
            results,
            "",
            "---",
            "",
            ablation,
            "",
            "---",
            "",
            statistics,
            "",
            "---",
            "",
            discussion,
            "",
            "---",
            "",
            threats,
            "",
            "---",
            "",
            reproducibility,
            "",
            "---",
            "",
            "## Tables and Figures",
            "",
            (
                "The following LaTeX tables and figure data "
                "are available for direct integration into "
                "the target manuscript."
            ),
            "",
            "### Tables",
            "",
            "- `tables/baseline_metrics.tex`",
            "- `tables/ablation_results.tex`",
            "- `tables/component_ranking.tex`",
            "",
            "### Figures",
            "",
            (
                "- `figures/"
                "component_contribution_ranking.png`"
            ),
            (
                "- `figures/"
                "component_contribution.csv`"
            ),
            "",
            "---",
            "",
            "## Evidence Status",
            "",
            (
                "The package is generated from stored "
                "evaluation evidence and audited paper "
                "artifacts."
            ),
            "",
            (
                "Unsupported claims are not promoted to "
                "established findings."
            ),
            "",
            (
                "Ablation results are interpreted as "
                "comparative contribution evidence rather "
                "than definitive causal estimates."
            ),
            "",
        ]

        return "\n".join(lines)

    def _build_latex_package(self):
        lines = [
            "% CRME Evidence-Backed Results Package",
            "% Automatically generated by PaperPackageBuilder",
            "",
            "\\section{Results}",
            "",
            (
                "The CRME evaluation pipeline was conducted "
                "across two datasets using five evaluation "
                "metrics."
            ),
            "",
            (
                "The comparative evaluation identified "
                "SRCB\\_v1 as the best-performing dataset "
                "across all reported metrics."
            ),
            "",
            "\\input{tables/baseline_metrics.tex}",
            "",
            "\\section{Ablation Study}",
            "",
            (
                "The ablation analysis evaluated the "
                "contribution of four architectural "
                "components under five configurations."
            ),
            "",
            (
                "The decisions component exhibited the "
                "largest aggregate degradation, followed "
                "by relations, sessions, and artifacts."
            ),
            "",
            "\\input{tables/ablation_results.tex}",
            "",
            "\\begin{figure}[htbp]",
            "\\centering",
            (
                "\\includegraphics[width=0.8\\linewidth]"
                "{figures/component_contribution_ranking.png}"
            ),
            (
                "\\caption{Component contribution ranking "
                "based on aggregate ablation degradation.}"
            ),
            "\\label{fig:crme-component-contribution}",
            "\\end{figure}",
            "",
            "\\section{Statistical Analysis}",
            "",
            (
                "The current evaluation package reports "
                "descriptive baseline metrics and "
                "component-level degradation values."
            ),
            "",
            (
                "No inferential statistical test, confidence "
                "interval, or hypothesis test is currently "
                "included in the evidence package."
            ),
            "",
            (
                "Accordingly, the present results should be "
                "interpreted as descriptive and comparative "
                "rather than as statistically significant "
                "evidence of superiority over a baseline."
            ),
            "",
            "\\section{Discussion}",
            "",
            (
                "The largest observed aggregate degradation "
                "was associated with the decisions component, "
                "whereas the artifacts component exhibited "
                "the lowest observed aggregate degradation."
            ),
            "",
            (
                "These results indicate heterogeneous "
                "component contribution within the evaluated "
                "CRME architecture."
            ),
            "",
            (
                "However, the observed ranking should be "
                "interpreted as an empirical comparative "
                "result rather than a definitive causal "
                "decomposition."
            ),
            "",
            "\\section{Threats to Validity}",
            "",
            (
                "The current evidence package does not include "
                "inferential statistical testing or confidence "
                "interval estimation."
            ),
            "",
            (
                "The ablation results provide comparative "
                "evidence of component contribution but do "
                "not establish definitive causal importance."
            ),
            "",
            (
                "Generalization to other datasets and research "
                "workflows requires further validation."
            ),
            "",
            "\\section{Reproducibility}",
            "",
            (
                "All reported results are derived from stored "
                "CRME evaluation artifacts."
            ),
            "",
            (
                "The evaluation pipeline preserves the "
                "relationship between raw evidence, aggregated "
                "summaries, paper tables, figures, and "
                "claim-level evidence."
            ),
            "",
            (
                "The Q1 consistency audit completed with "
                "overall status \\texttt{PASS}."
            ),
            "",
            (
                "Raw evaluation outputs are preserved and are "
                "not modified by the paper generation layer."
            ),
            "",
        ]

        return "\n".join(lines)

    def _build_manifest(self):
        return {
            "created_at": datetime.now(
                timezone.utc
            ).isoformat(),
            "builder": (
                "PaperPackageBuilder"
            ),
            "source_directory": self.paper_dir,
            "outputs": [
                "CRME_RESULTS_PACKAGE.md",
                "CRME_RESULTS_PACKAGE.tex",
            ],
            "source_sections": [
                "sections/results.md",
                "sections/ablation.md",
                "sections/statistics.md",
                "sections/discussion.md",
                "sections/threats_to_validity.md",
                "sections/reproducibility.md",
            ],
            "source_tables": [
                "tables/baseline_metrics.tex",
                "tables/ablation_results.tex",
                "tables/component_ranking.tex",
            ],
            "source_figures": [
                (
                    "figures/"
                    "component_contribution_ranking.png"
                ),
                (
                    "figures/"
                    "component_contribution.csv"
                ),
            ],
            "status": "completed",
        }

    def build(self):
        markdown = self._build_package()

        latex = self._build_latex_package()

        markdown_path = os.path.join(
            self.output_dir,
            "CRME_RESULTS_PACKAGE.md",
        )

        latex_path = os.path.join(
            self.output_dir,
            "CRME_RESULTS_PACKAGE.tex",
        )

        manifest_path = os.path.join(
            self.output_dir,
            "package_manifest.json",
        )

        with open(
            markdown_path,
            "w",
            encoding="utf-8",
        ) as file:
            file.write(markdown)

        with open(
            latex_path,
            "w",
            encoding="utf-8",
        ) as file:
            file.write(latex)

        with open(
            manifest_path,
            "w",
            encoding="utf-8",
        ) as file:
            json.dump(
                self._build_manifest(),
                file,
                indent=4,
            )

        return {
            "markdown": markdown_path,
            "latex": latex_path,
            "manifest": manifest_path,
            "output_dir": self.output_dir,
            "status": "completed",
        }
